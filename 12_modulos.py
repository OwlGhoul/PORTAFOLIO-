"""# importando un modulo"""

#Modulo para trabajar con operaciones matematicas
import math
print(math.pi)
print(math.sqrt(16))
print(math.pow(2,3))
print(math.floor(9.84))

#podemos importar funciones especificas de cada modulo
from math import pi, sqrt, pow
print(pi)
print(sqrt(16))
print(pow(2,3))
print(math.floor(9.84))

#tambien podemos importar todas las funciones de golpe
from math import *
print(pi)
print(sqrt(16))
print(pow(2,3))
print(math.floor(9.84))

#tambien podemos renombrar una funcion
from math import pi as numPI
print(numPI)

